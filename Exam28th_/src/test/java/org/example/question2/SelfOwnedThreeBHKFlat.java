package org.example.question2;

public class SelfOwnedThreeBHKFlat {
    private float length;
    private float breadth;
    private float height;
    private float finalPriceOfFlat;

    public float getLength() {
        return length;
    }

    public void setLength(float length) {
        this.length = length;
    }

    public float getBreadth() {
        return breadth;
    }

    public void setBreadth(float breadth) {
        this.breadth = breadth;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public float getFinalPriceOfFlat() {
        return finalPriceOfFlat;
    }

    public void setFinalPriceOfFlat(float finalPriceOfFlat) {
        this.finalPriceOfFlat = finalPriceOfFlat;
    }
}
